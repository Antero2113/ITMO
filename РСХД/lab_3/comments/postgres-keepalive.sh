#!/usr/bin/env bash
set -euo pipefail
# set -e: выход при любой ошибке (ненулевой код возврата)
# set -u: ошибка при использовании неопределенной (unset) переменной
# set -o pipefail: пайп считается завершённым с ошибкой, если любая команда в нем завершилась с ошибкой

child_pid=""  # Переменная для хранения PID дочернего процесса PostgreSQL

# Функция, вызываемая при завершении контейнера
shutdown() {
  # -n: проверяет, не пуста ли строка child_pid
  # kill -0: не отправляет сигнал, а только проверяет, существует ли процесс
  # 2>/dev/null: перенаправляет stderr в никуда (подавляет ошибки)
  if [ -n "${child_pid}" ] && kill -0 "$child_pid" 2>/dev/null; then
    kill -TERM "$child_pid" 2>/dev/null || true
    # -TERM: сигнал вежливого завершения (аналог Ctrl+C)
    # || true: игнорирует ошибку, если процесс уже завершился
    wait "$child_pid" || true
    # wait: ожидает завершения указанного процесса
    # Блокирует выполнение, пока процесс не закроется
  fi
  exit 0  # Выход с кодом 0 (успешное завершение)
}

# trap: перехватывает сигналы и выполняет указанную команду/функцию
# SIGTERM (15): сигнал по умолчанию от команды docker stop
# SIGINT (2): сигнал от Ctrl+C (интерактивное прерывание)
trap shutdown SIGTERM SIGINT

# "$@": передает все аргументы, полученные скриптом, внутрь entrypoint'а
# &: запускает процесс в фоновом режиме (не блокирует выполнение скрипта)
/usr/local/bin/docker-entrypoint.sh "$@" &
child_pid=$!  # $! - специальная переменная, содержащая PID последнего фонового процесса

wait "$child_pid" || true
# wait: блокирует выполнение скрипта, пока не завершится PostgreSQL
# || true: продолжает выполнение даже если wait вернул ошибку

# БЕСКОНЕЧНЫЙ ЦИКЛ (костыль, чтобы контейнер не завершался)
while true; do
  sleep 3600  # sleep: приостанавливает выполнение на 3600 секунд (1 час)
done