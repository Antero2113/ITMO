#!/usr/bin/env bash
# Скрипт ожидания готовности нескольких PostgreSQL серверов
# Используется в CMD: wait-for-services.sh pg_a 5432 pg_b 5432 pg_c 5432

set -euo pipefail
# set -e: выход при любой ошибке
# set -u: ошибка при неопределённой переменной
# set -o pipefail: ошибка, если любая команда в пайпе провалилась

# === ОБРАБОТКА АРГУМЕНТОВ ===
# "$#" - количество переданных аргументов
# -gt 0 - пока количество аргументов больше нуля
while [ "$#" -gt 0 ]; do
  # $1 - первый аргумент (хост)
  # $2 - второй аргумент (порт)
  host="$1"
  port="$2"
  
  shift 2
  # shift - сдвигает аргументы влево
  # shift 2 - удаляет первые два аргумента (host и port)
  # Пример: было (pg_a 5432 pg_b 5432) → стало (pg_b 5432)
  
  echo "[wait] ${host}:${port}"
  
  # === ОЖИДАНИЕ ГОТОВНОСТИ PostgreSQL ===
  # pg_isready - утилита для проверки состояния PostgreSQL
  #   -h: хост (host) для подключения
  #   -p: порт (port)
  #   -U: пользователь (user) - postgres
  #   -d: база данных (database) - postgres
  # 
  # >/dev/null - перенаправить stdout (стандартный вывод) в никуда
  # 2>&1 - перенаправить stderr (поток ошибок) туда же (тоже в никуда)
  #   Итог: все сообщения pg_isready подавляются
  #
  # until ... do ... done - цикл, который выполняется ПОКА команда НЕ вернёт успех (код 0)
  # pg_isready возвращает 0, когда PostgreSQL готов принимать подключения
  until pg_isready -h "$host" -p "$port" -U postgres -d postgres >/dev/null 2>&1; do
    sleep 2   # Пауза 2 секунды между попытками
  done
done
# Когда цикл завершится, все указанные сервера готовы к работе